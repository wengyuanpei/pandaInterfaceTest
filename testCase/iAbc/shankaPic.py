import qrcode
from PIL import ImageFont, ImageDraw, Image



def shankaTest(qrdataName:str,qrdata:str):
    qr = qrcode.QRCode(version=2,
                       error_correction=qrcode.constants.ERROR_CORRECT_H,
                       )
    #生成二维码
    qr.add_data(qrdata)
    qr.make(fit=True)
    Img=qr.make_image()
    #保存图片的位置
    savepath=r"C:\Users\zhang\Documents\pandaInterfaceTest\testCase\星球闪卡照片\%s.png" %(qrdataName)
    Img.save(savepath)
    img = Image.open(savepath)
    print('二维码大小：',img.size)
    draw = ImageDraw.Draw(img)
    ttfront = ImageFont.truetype('msyh.ttc', 40,0)  # 字体文件msyh.ttc，需要查找下载

    content = str(qrdataName)
    width_w, height_h = ttfront.getsize(content)
    print('二维码内容：',content)
    draw.text(((330-width_w)/2, 280), content, font=ttfront)  # 文字位置，正文内容，文字RGB颜色，字体
    img.save(savepath)


